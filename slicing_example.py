# List Slicing
names = ["sri", "Tamil", "Subbu", "Suresh", "Siva"]

print(names[0:4])
print(names[2:])
print(names[:3])
print(names[0:4:2])
print(names[::2])
print(names[::-1])

# Tuple Slicing
